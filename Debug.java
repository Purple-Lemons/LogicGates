package logicgates;

public class Debug {
    public Debug(){

    }

    public void stringOut(String output){
        System.out.println(output);
    }

    public void intOut(int output){
        System.out.println(output);
    }

    public void booleanOut(boolean output){
        System.out.println(output);
    }

    public void objectOut(Object output){
        System.out.println(output);
    }
}
